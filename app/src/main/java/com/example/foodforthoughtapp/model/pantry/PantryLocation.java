package com.example.foodforthoughtapp.model.pantry;

public class PantryLocation {

    public String street;
    public String city;
    public String zip;

    public PantryLocation() {}

    public PantryLocation(String street, String city, String zip) {
        this.street = street;
        this.city = city;
        this.zip = zip;
    }
}
